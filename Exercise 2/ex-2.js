
let a = 15;
let b = "15";

// Equal to
console.log(a == b);

// Not equal to
console.log(a != b);

// Strict equal to
console.log(a === b);

// Strict not equal to
console.log(a !== b);

// Greater than
console.log(a > b);

// Less than
console.log(a < b);

// Greater than or equal to
console.log(a >= b);

// Less than or equal to
console.log(a <= b);